# Program to demonstrate the use of all six math functions

# Addition
num1 = 2
num2 = 2
sum = num1 + num2
print("ADDITION: {}+{}={}".format(num1, num2, sum))

# Subtraction
num1 = 4
num2 = 2
diff = num1 - num2
print("SUBTRACTION: {}-{}={}".format(num1, num2, diff))

# Multiplication
num1 = 4
num2 = 2
prod = num1 * num2
print("MULTIPLICATION: {}*{}={}".format(num1, num2, prod))

# Division
num1 = 4
num2 = 2
quotient = num1 / num2
print("DIVISION: {}/{}={}".format(num1, num2, quotient))

# Exponent
num1 = 2
num2 = 3
power = num1 ** num2
print("EXPONENT: {}**{}={}".format(num1, num2, power))

# Remainder
num1 = 5
num2 = 2
rem = num1 % num2
print("REMAINDER: {}%{}={}".format(num1, num2, rem))
